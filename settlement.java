    private void jTextField2KeyReleased(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_jTextField2KeyReleased
        if (Character.isDigit(evt.getKeyChar()) | evt.getKeyCode() == KeyEvent.VK_ENTER
                | evt.getKeyCode() == KeyEvent.VK_BACK_SPACE | evt.getKeyCode() == KeyEvent.VK_DELETE | evt.getKeyCode() == KeyEvent.VK_PERIOD
                | evt.getKeyCode() == KeyEvent.VK_DOWN | evt.getKeyCode() == KeyEvent.VK_LEFT | evt.getKeyCode() == KeyEvent.VK_RIGHT) {
            jTextField2.setEditable(true);

            if (jRadioButton3.isSelected()) {
                if (!jTextField2.getText().equals("") && !jTextField7.getText().equals("")) {
                    jTextField8.setText(Double.toString(Double.parseDouble(jTextField2.getText()) - Double.parseDouble(jTextField7.getText())));
                }
            } else if (jRadioButton4.isSelected()) {
                if (!jTextField2.getText().equals("") && !jTextField6.getText().equals("")) {
                    jTextField8.setText(Double.toString(Double.parseDouble(jTextField2.getText()) - Double.parseDouble(jTextField6.getText())));
                }
            } else if (jRadioButton5.isSelected()) {
                if (!jTextField2.getText().equals("") && !jTextField6.getText().equals("")) {
                    jTextField8.setText(Double.toString(Double.parseDouble(jTextField2.getText()) - Double.parseDouble(jTextField6.getText())));
                }
            }

        } else {
            jTextField2.setEditable(false);
            JOptionPane.showMessageDialog(this, "Invalid Key", "ERROR!", JOptionPane.ERROR_MESSAGE, erricon);
            jTextField2.setText("");
            jTextField2.grabFocus();
        }
    }//GEN-LAST:event_jTextField2KeyReleased

