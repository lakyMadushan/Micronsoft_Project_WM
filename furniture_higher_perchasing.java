    public void tableload() {
        DefaultTableModel dtm = (DefaultTableModel) jTable1.getModel();
        dtm.setRowCount(0);
        try {
            ResultSet rs = DB.search("select * from f_h_p");
            while (rs.next()) {
                Vector v = new Vector();
                v.add(rs.getString("id"));
                v.add(rs.getString("f_id"));
                v.add(rs.getString("f_name"));
                v.add(rs.getString("f_cost"));
                v.add(rs.getString("f_s_price"));
                v.add(rs.getString("in_rate"));
                v.add(rs.getString("d_pay"));
                v.add(rs.getString("period"));
                v.add(rs.getString("l_amount"));
                v.add(rs.getString("monthly_ins"));
                v.add(rs.getString("balance"));
                v.add(rs.getString("cus_id"));
                v.add(rs.getString("cus_name"));
                v.add(rs.getString("cus_tp"));
                v.add(rs.getString("cus_add"));
                dtm.addRow(v);
            }
        } catch (Exception e) {
            e.printStackTrace();
        }
    }

