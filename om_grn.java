    private void jTextField5KeyReleased(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_jTextField5KeyReleased
        if (Character.isDigit(evt.getKeyChar()) | evt.getKeyCode() == KeyEvent.VK_ENTER
                | evt.getKeyCode() == KeyEvent.VK_BACK_SPACE | evt.getKeyCode() == KeyEvent.VK_DELETE
                | evt.getKeyCode() == KeyEvent.VK_DOWN | evt.getKeyCode() == KeyEvent.VK_LEFT | evt.getKeyCode() == KeyEvent.VK_RIGHT) {
            jTextField5.setEditable(true);

            if (!jTextField4.getText().equals("") && !jTextField5.getText().equals("")) {
                jTextField6.setText(df.format(Double.parseDouble(jTextField4.getText()) * Double.parseDouble(jTextField5.getText())));
            }
        } else {
            jTextField5.setEditable(false);
            JOptionPane.showMessageDialog(this, "Invalid Key", "ERROR!", JOptionPane.ERROR_MESSAGE, erricon);
            jTextField5.setText("");
            jTextField5.grabFocus();
        }

    }//GEN-LAST:event_jTextField5KeyReleased

    private void jTable1KeyPressed(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_jTable1KeyPressed
        if (evt.getKeyCode() == KeyEvent.VK_ENTER) {
            try {
                DefaultTableModel dtm = (DefaultTableModel) jTable1.getModel();
                if (jTable1.getSelectedRow() != -1) {

                    String g_id = dtm.getValueAt(jTable1.getSelectedRow(), 0).toString();
                    String grn_id = dtm.getValueAt(jTable1.getSelectedRow(), 1).toString();
                    String i_id = dtm.getValueAt(jTable1.getSelectedRow(), 2).toString();
                    String i_name = dtm.getValueAt(jTable1.getSelectedRow(), 3).toString();
                    Double i_cost = Double.parseDouble(dtm.getValueAt(jTable1.getSelectedRow(), 4).toString());
                    Integer qty = Integer.parseInt(dtm.getValueAt(jTable1.getSelectedRow(), 5).toString());
                    Double amount = Double.parseDouble(dtm.getValueAt(jTable1.getSelectedRow(), 6).toString());

                    //stock
                    ResultSet r1 = DB.search("select * from o_m_stock where id = '" + i_id + "' ");
                    if (r1.next()) {
                        jTextField12.setText(r1.getString("qty"));
                    }

                    //grn data
                    ResultSet rs = DB.search("select * from o_m_grn where grn_id = '" + grn_id + "' ");
                    if (rs.next()) {
                        jTextField7.setText(rs.getString("total_amount"));
                        jTextField8.setText(rs.getString("payment"));
                        jTextField9.setText(rs.getString("balance"));
                        jTextField10.setText(rs.getString("remark"));
                        jComboBox1.setSelectedItem(rs.getString("supplier"));
                    }

                    //grn items
                    //jTextField11.setText(g_id);
                    jLabel15.setText(grn_id);
                    jTextField2.setText(i_id);
                    jTextField3.setText(i_name);
                    jTextField4.setText(i_cost.toString());
                    jTextField5.setText(qty.toString());
                    jTextField6.setText(amount.toString());

                    jButton2.setEnabled(false);
                } else {
                    System.out.println("No data found for ID: " + jLabel15.getText());
                }
            } catch (Exception e) {
                e.printStackTrace();
            }
        }
    }//GEN-LAST:event_jTable1KeyPressed

