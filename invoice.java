                double sum1 = 0;
                for (int i = 0; i < dtm.getRowCount(); i++) {
                    sum1 = sum1 + Double.parseDouble(dtm.getValueAt(i, 5).toString());
                    jTextField15.setText(String.valueOf(sum1));
                }

                Toolkit.getDefaultToolkit().beep();
                jTextField1.setText("");
                jTextField2.setText("");
                jTextField4.setText("");
                jTextField3.setText("");
