    private void tableLoad() {
        try {
            clear();
            DefaultTableModel dtm = (DefaultTableModel) jTable1.getModel();
            dtm.setRowCount(0);
            ResultSet rs = DB.search("select * from grn_item order by g_id DESC");
            while (rs.next()) {
                Vector<Object> v = new Vector<>();
                v.add(rs.getString("g_id"));
                v.add(rs.getString("grn_id"));
                v.add(rs.getString("item_id"));
                v.add(rs.getString("i_name"));
                v.add(rs.getString("i_cost"));
                v.add(rs.getString("qty"));
                v.add(rs.getString("amount"));
                //v.add(rs.getString("time"));
                dtm.addRow(v);
            }
            jButton1.setEnabled(true);
        } catch (Exception e) {
            e.printStackTrace();
            JOptionPane.showMessageDialog(this, "Table loading Unsuccesfully !", "ERROR", JOptionPane.INFORMATION_MESSAGE, erricon);
        }
    }
    
