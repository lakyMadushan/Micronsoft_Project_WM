    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        DefaultTableModel dtm = (DefaultTableModel) jTable1.getModel();
        if (dtm.getRowCount() > 0) {
            // Save
            try {
                for (int i = 0; i < dtm.getRowCount(); i++) {

                    String i_id = dtm.getValueAt(i, 0).toString();
                    String id = dtm.getValueAt(i, 1).toString();
                    String wood = dtm.getValueAt(i, 2).toString();
                    String qty = dtm.getValueAt(i, 3).toString();
                    String cost = dtm.getValueAt(i, 4).toString();
                    String amount = dtm.getValueAt(i, 5).toString();

                    ResultSet rs = DB.search("select * from prermit_s where prermit_id = '" + id + "' ");
                    if (rs.next()) {
                        JOptionPane.showMessageDialog(this, "This id already added to Table : " + id, "ERROR", JOptionPane.INFORMATION_MESSAGE);
                    } else {
                        ResultSet r1 = DB.search("select * from wood_s where wood_t = '" + wood + "' ");
                        if (r1.next()) {
                            String w_id = r1.getString("wt_id");

                            DB.iud("insert into prermit_s (p_id,prermit_id,wood_t,qty,cost,amount,wood_id) "
                                    + "values ( '" + i_id + "','" + id + "','" + wood + "','" + qty + "','" + cost + "','" + amount + "','" + w_id + "' )");
                        }
                        JOptionPane.showMessageDialog(this, "Insert successfully.", "DONE", JOptionPane.INFORMATION_MESSAGE, icon);
                    }
                }

                clear();
                tableLoad();
            } catch (Exception e) {
                e.printStackTrace();
            }
        } else {
            JOptionPane.showMessageDialog(this, "Enter Data  !", "ERROR", JOptionPane.INFORMATION_MESSAGE);
        }
    }//GEN-LAST:event_jButton1ActionPerformed

