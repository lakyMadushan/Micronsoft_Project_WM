    private void jTextField6KeyReleased(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_jTextField6KeyReleased
        try {
            ResultSet rs = DB.search("select * from f_stock where f_name like '%" + jTextField6.getText() + "%'");
            ResultSet rs1 = DB.search("select * from wood_s where wood_t like '%" + jTextField6.getText() + "%'");
            ResultSet rs2 = DB.search("select * from prermit_s where wood_t like '%" + jTextField6.getText() + "%'");
            Vector v1 = new Vector();
            while (rs.next()) {
                v1.add(rs.getString("f_id") + ":" + rs.getString("f_name") + "/" + rs.getString("cost") + "/" + rs.getString("f_id") + "/" + rs.getString("f_name") + "/" + rs.getString("qty"));
            }
            while (rs1.next()) {
                v1.add(rs1.getString("w_id") + ":" + rs1.getString("wood_t") + "/" + rs1.getString("cost") + "/" + rs1.getString("w_id") + "/" + rs1.getString("wood_t") + "/" + rs1.getString("qty"));
            }
            while (rs2.next()) {
                v1.add(rs2.getString("p_id") + ":" + rs2.getString("wood_t") + "/" + rs2.getString("cost") + "/" + rs2.getString("p_id") + "/" + rs2.getString("wood_t") + "/" + rs2.getString("qty"));
            }
            jList1.setListData(v1);
        } catch (Exception e) {
            e.printStackTrace();
        }
    }//GEN-LAST:event_jTextField6KeyReleased

