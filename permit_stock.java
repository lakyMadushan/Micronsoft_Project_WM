    private void tableLoad() {
        try {
            clear();
            DefaultTableModel dtm = (DefaultTableModel) jTable1.getModel();
            dtm.setRowCount(0);
            ResultSet rs = DB.search("select * from prermit_s order by id ASC");
            while (rs.next()) {
                Vector<Object> v = new Vector<>();
                v.add(rs.getString("p_id"));
                v.add(rs.getString("prermit_id"));
                v.add(rs.getString("wood_t"));
                v.add(rs.getString("qty"));
                v.add(rs.getString("cost"));
                v.add(rs.getString("amount"));
                dtm.addRow(v);
            }
            jButton1.setEnabled(true);
        } catch (Exception e) {
            e.printStackTrace();
            JOptionPane.showMessageDialog(this, "Table loading Unsuccesfully !", "ERROR", JOptionPane.INFORMATION_MESSAGE, erricon);
        }
    }

